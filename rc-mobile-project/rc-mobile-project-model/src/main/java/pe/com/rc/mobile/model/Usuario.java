package pe.com.rc.mobile.model;

public class Usuario {

	private String id;
	private String email;
	private String compartirUbicacion;
	private String numeroTelefono;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCompartirUbicacion() {
		return compartirUbicacion;
	}

	public void setCompartirUbicacion(String compartirUbicacion) {
		this.compartirUbicacion = compartirUbicacion;
	}

	public String getNumeroTelefono() {
		return numeroTelefono;
	}

	public void setNumeroTelefono(String numeroTelefono) {
		this.numeroTelefono = numeroTelefono;
	}

}
